/**
 * 某老师现要录入班级学生信息（姓名，性别，年龄，成绩），学生信息存储在数组中。请使用方法完成
 */

public class Student {
    public String name;

    public String sex;

    public int age;

    public int score;

    public Student(String name, String sex, int age, int score) {
        this.name = name;
        this.sex = sex;
        this.age = age;
        this.score = score;

    }

}
